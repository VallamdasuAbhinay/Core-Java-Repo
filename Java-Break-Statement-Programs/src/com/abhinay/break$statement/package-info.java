package com.abhinay.break$statement;
/*
 * Break statements in java are used to end the ongoing process. They can be
 * used to terminate for loops, while loops and do-while loops. They are also
 * used to come out of the switch block once the matching case has been found.
 * 
 * The break statement in the programming world is used to terminate or end an
 * ongoing process.
 * 
 * The break statement in java is given by the break keyword, the syntax is
 * given as:
 * 
 * break;
 * 
 * Break statements help us in terminating a loop or breaking from a switch-case
 * construct. 
 * 
 * In the case of nested loops, break statements terminate the
 * innermost loop they are present in. 
 * 
 * The working of break statements for all
 * types of loops is the same. 
 * 
 * In the case of nested loops if we wish to break
 * from a specific loop, then we use labels. We can then name each loop and can
 * terminate whichever loop we want to. 
 * 
 * In switch case construct, the break
 * statements help in moving out of the code structure once the case block has
 * been executed. 
 * 
 * In the absence of break statements, once a case block has been
 * executed, all the other cases below it also get executed until a break
 * statement is encountered.
 * 
 */